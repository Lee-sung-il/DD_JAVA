package day02.src.day02;
//1.준비물: 입력기능을 내코드에 연결 시켜줘
import java.util.Scanner;
public class Day02input {
    public static void main(String[] args) {
//        2.준비물 : Scanner를 sc라는 이름으로 사용하겠다.
        Scanner sc = new Scanner(System.in);
//        문자열로 입력받아라 -> 기능자체가 키보드로 입력한 입력값
//        System.out.println(sc.next());
//        sc.nextInt(): 정수값으로 입력받아라
//        System.out.println(sc.nextInt());
//        sc.nextFloat(): 실수형값으로 입력받아라
//        System.out.println(sc.nextFloat());
//        sc.nextLine: 문자열로 뛰어쓰기까지 입력받아라
        System.out.println(sc.nextLine());
/*
Console 창에 네모버튼이 빨간색: 실행중, 회색: 실행종료
커서가 깜빡거린다면 입력받을 준비가 되어있다
 */
    }
}
