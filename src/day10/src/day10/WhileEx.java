package day10.src.day10;

public class WhileEx {
    public static void main(String[] args) {
        /*
        while 문 사용시
        1)변수 생성
        2)조건식에서 변수의 범위 설정
        3)반복 할 명령 적기
        4)변수의 값을 변화
         */
        int x = 1;
        while (x <= 10){
            System.out.println("x :"+x);
            x++;
        }

    }
}
